package org.itcgae.siga.DTOs.cen;

public class DatosBeneficiarioMutualidadDTO {

	private String idPoliza;
	private String textoOtros;
	private int idTipoBeneficiario;
	private String beneficiario;
	
	
	public String getIdPoliza() {
		return idPoliza;
	}
	public void setIdPoliza(String idPoliza) {
		this.idPoliza = idPoliza;
	}
	public String getTextoOtros() {
		return textoOtros;
	}
	public void setTextoOtros(String textoOtros) {
		this.textoOtros = textoOtros;
	}
	public int getIdTipoBeneficiario() {
		return idTipoBeneficiario;
	}
	public void setIdTipoBeneficiario(int idTipoBeneficiario) {
		this.idTipoBeneficiario = idTipoBeneficiario;
	}
	public String getBeneficiario() {
		return beneficiario;
	}
	public void setBeneficiario(String beneficiario) {
		this.beneficiario = beneficiario;
	}
	
	
}
